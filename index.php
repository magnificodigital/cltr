			function validaForm()
			{
				var f = document.formContato;
				
				if(f.email.value == 'Email*')
				{
					alert('Por favor digite seu email.');
					return false;
				}
				
				if (f.email.value.indexOf("@") == -1)
				{
					alert("Digite o seu E-mail Corretamente.");
					return false;
				}
				if (f.email.value.indexOf(".") == -1)
				{
					alert("Digite o seu E-mail Corretamente.");
					return false;
				}
				
				if(f.mensagem.value == 'Mensagem')
				{
					alert('Por favor digite sua mensagem.');
					return false;
				}
